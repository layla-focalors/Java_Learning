
import java.util.*;

public class Example {
    public static void main(String[] args) {
        // ------------------------------------------------------------------
        // 019_operator02 예제의 목표는 
        // 단항 연산자인 부호 연산자를 직접 실습.
        // ------------------------------------------------------------------

        int k = 10;
        System.out.println("+k = " + +k);
        System.out.println("-k = " + -k);

        System.out.println("k 출력 > " + k); // 원래 k값에는 변화가 없음
        return;
    }
}



