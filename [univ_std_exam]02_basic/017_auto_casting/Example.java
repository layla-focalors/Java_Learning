
import java.util.*;

public class Example {
    public static void main(String[] args) {
        
        // ------------------------------------------------------------------
        // 017_auto_casting 예제의 목표는 
        // 자동 형 변환의 예시를 실습하고, 어느 때 자동 형 변환이 발생하는지 파악.
        // ------------------------------------------------------------------
        
        // 올바른 자동 형 변환 예시
        int int_value = 100;
        double double_value = 111.45;

        // 올바른 자동 형 변환 예시
        long long_number1 = 10l;
        int int_number2 = 20;

        // 올바르지 못한 자동 형 변환 예시
        char char_value1 = 'a';
        char char_value2 = 1;

        return;
    }
}



